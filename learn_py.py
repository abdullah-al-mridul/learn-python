import sys

# if statement
if 10 > 8:
    print(sys.version)
    print("Hello, World!")

# variable assignment
x = 5
y = "John"
print(x, y)

# python statement
print("Hello World!")
print("Have a good day.")
print("Learning Python is fun!")

# semicolons
a = 5; b = 10; print(a + b)

# quotes
single_quote = 'Hello'
double_quote = "World"
print(single_quote, double_quote)

# print without newline
print("Hello" , end=" "); print("World!")